//LANG : C++
#include<bits/stdc++.h>
using namespace std;

int main(){
	ios::sync_with_stdio(0); cin.tie(0);
	char c;
	cin >> c;
	if(c=='a'){
		cout << "animal";
	}
	else if(c=='A'){
		cout << "ANIMAL";
	}
	else if(c=='b'){
		cout << "banana";
	}
	else if(c=='B'){
		cout << "BANANA";
	}
	else{
		cout << "ERROR";
	}		
}
